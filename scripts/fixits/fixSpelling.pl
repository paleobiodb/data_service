sub inSpellings {
    my $name = shift;
    my $parent_no = shift;

    my $sql = "SELECT child_spelling_no, taxon_name FROM opinions, authorities where opinions.child_spelling_no=authorities.taxon_no and opinions.status IN ('recombined as','corrected as','rank changed as') AND parent_no=$parent_no AND taxon_name='$name'";
    my @results = @{$dbt->getData($sql)};
    if (@results) {
        return 1;
    } else {
        return 0;
    }
}

sub printOp {
